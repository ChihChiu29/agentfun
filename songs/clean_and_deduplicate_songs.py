for title in song_titles_raw:
    # Remove content within parentheses and square brackets
    title = re.sub(r'\(.*?\)', '', title)
    title = re.sub(r'\[.*?\]', '', title)
    # Remove text after ' - '
    title = title.split(' - ')[0]
    # Remove '《' and '》' characters
    title = title.replace('《', '').replace('》', '')
    # Remove leading/trailing whitespace
    title = title.strip()
    if title: # Only add if title is not empty after cleaning
        cleaned_titles.add(title)

